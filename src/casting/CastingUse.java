package casting;

public class CastingUse {

	public static void main(String[] args) {



		int a=11;
		System.out.println(a);//11
		double b=a;
		System.out.println(b);//11.0

		System.out.println("============================");	
		double d=22;
		System.out.println(d);
		int e=(int)d;
		System.out.println(e);//22.0
	}

}
