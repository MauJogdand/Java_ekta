package myPackage;

public class SwitchCase {

	public static void main(String[] args) {


		int a;
		a=1;
				switch (a) {
				case 1: {

					System.out.println("Case one");
					break;
				}
				case 2:
				{
					System.out.println("Case two");
					break;
					
				}
				default:
					System.out.println("Default");				}
	}

}
